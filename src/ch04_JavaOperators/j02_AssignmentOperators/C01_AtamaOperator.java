package ch04_JavaOperators.j02_AssignmentOperators;

public class C01_AtamaOperator {
    public static void main(String[] args) {

        //    =	        x = 8	    x = 8
        //    +=	    x += 3	    x = x + 3
        //    -=	    x -= 3	    x = x - 3
        //    *=	    x *= 3	    x = x * 3
        //    /=	    x /= 3	    x = x / 3
        //    %=	    x %= 3	    x = x % 3


        int yas =33;// create edilen yas variable ına 33 degeri atandı.

        yas += 5; // yas 5 artırıldı ve atandı

        yas*= 2; //yas 2 ile carpilip degerine atandı

        yas/= 2; // yas 2 ye bölünüp degerine atandı

        System.out.println("yas = " + yas);// yas =38


    }
}
